#include <unistd.h>

void Rush(int x, int y)
{
	char sim;
	int X;
	int Y;

	X = 0;
	Y = 0;

	while(X < y)
	{
		while(Y < x)
		{
			if ((X == 0 || X == y-1) && (Y == 0 || Y == x-1))
				{
					sim = 'o';
				}
				else if(X == 0 || X == y -1)
				{
					sim = '-';
				}
				else if(Y == 0 || Y == x-1)
				{
					sim = '|';
				}
				else
				{
					sim = ' ';
				}
				Y++;
				write(1,&sim,1);
		}
		write(1," ",1);
		X++;
		Y = 0;
	}
}
int		main()
{
	Rush(5,3);
}

